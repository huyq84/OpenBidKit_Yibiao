// ============================================================
// 知识库素材注入
// ============================================================

function injectKnowledgeContent(knowledgeContents: string[]): string {
  if (!knowledgeContents || knowledgeContents.length === 0) return '';
  return `\n\n## 参考知识库素材\n以下素材来自企业内部知识库，可作为编写参考，但不要直接抄袭：\n${knowledgeContents.map((c, i) => `\n### 素材${i + 1}\n${c}`).join('\n')}`;
}

// ============================================================
// 主函数
// ============================================================

export interface BuildChapterContentMessagesInput {
  chapter: OutlineItem;
  parentChapters?: OutlineItem[];
  siblingChapters?: OutlineItem[];
  projectOverview?: string;
  knowledgeContents?: string[];  // 知识库正文素材
  tableRequirement?: 'none' | 'light' | 'moderate' | 'heavy';
}

export function buildChapterContentMessages(input: BuildChapterContentMessagesInput): ChatMessage[] {
  const {
    chapter,
    parentChapters = [],
    siblingChapters = [],
    projectOverview,
    knowledgeContents = [],
    tableRequirement = 'moderate',
  } = input;

  const chapterType = classifyChapter(chapter, parentChapters);
  const systemContent = chapterTypeSystemPrompt(chapterType, chapter);

  const parentContext = parentChapters.length > 0
    ? `\n\n## 上级章节\n${parentChapters.map(p => `- ${p.title}${p.description ? `：${p.description}` : ''}`).join('\n')}`
    : '';

  const siblingContext = siblingChapters.length > 0
    ? `\n\n## 同级章节（本章无需重复已有内容）\n${siblingChapters.map(s => `- ${s.title}`).join('\n')}`
    : '';

  const overviewContext = projectOverview
    ? `\n\n## 项目概述\n${projectOverview}`
    : '';

  const knowledgeContext = injectKnowledgeContent(knowledgeContents);

  const tableNote = tableRequirement !== 'none'
    ? `\n\n## 插图/表格要求\n本章节建议插入 ${tableRequirement === 'heavy' ? '大量' : tableRequirement === 'moderate' ? '适量' : '少量'} 表格或示意图来说明关键技术参数、施工流程或配置清单。`
    : '';

  return [
    { role: 'system', content: systemContent },
    { role: 'user', content: `请为以下章节生成正文内容。${overviewContext}${parentContext}${siblingContext}${knowledgeContext}${tableNote}` },
  ];
}

