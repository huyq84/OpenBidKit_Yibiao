import type { ChatMessage, OutlineItem } from '../types';

// ============================================================
// 施工组织设计章节分类
// ============================================================

type ChapterType =
  | 'project_overview'       // 工程概况
  | 'construction_plan'      // 施工方案/方法
  | '进度计划'               // 施工进度计划
  | 'quality_control'         // 质量控制
  | 'safety_management'       // 安全管理
  | 'site_layout'            // 施工现场平面布置
  | 'equipment_management'    // 机械设备管理
  | 'labor_plan'             // 劳动力配置
  | 'cost_control'           // 降低成本措施
  | 'environmental'          // 文明施工/环境保护
  | 'seasonal'               // 季节性施工
  | 'acceptance'             // 验收条件
  | 'technical_management'    // 技术管理体系
  | 'risk_management'        // 风险管理
  | 'service_support'        // 售后服务
  | 'other'                  // 其他

function classifyChapter(chapter: OutlineItem, parentChapters: OutlineItem[]): ChapterType {
  const title = (chapter.title || '').toLowerCase();
  const desc = (chapter.description || '').toLowerCase();
  const parentTitle = parentChapters.map(p => (p.title || '').toLowerCase()).join(' ');

  if (/工程概况|项目概述|项目简介/.test(title)) return 'project_overview';
  if (/施工方案|施工方法|施工工艺|分部分项|基础工程|主体结构|装饰装修|安装工程|钢结构|防水工程|给排水|电气|通风|消防|管网/.test(title + desc)) return 'construction_plan';
  if (/进度|工期|计划|网络图|横道图|甘特|节点/.test(title + desc)) return '进度计划';
  if (/质量|创优|验收|标准|规范|控制措施|保证体系/.test(title + desc)) return 'quality_control';
  if (/安全|防护|应急|风险|危险源|消防|用电/.test(title + desc)) return 'safety_management';
  if (/平面布置|临设|临建|场地|交通|材料堆场|塔吊/.test(title + desc)) return 'site_layout';
  if (/机械|设备|塔吊|施工电梯|挖掘机|混凝土/.test(title + desc)) return 'equipment_management';
  if (/劳动力|人工|工种|劳务|班组|人员配置/.test(title + desc)) return 'labor_plan';
  if (/成本|经济|节约|降低/.test(title + desc)) return 'cost_control';
  if (/文明|环保|绿色|扬尘|噪音|环境保护|节能/.test(title + desc)) return 'environmental';
  if (/季节|雨季|夏季|冬季|高温|低温|防暑|防冻/.test(title + desc)) return 'seasonal';
  if (/验收|竣工|交付|成品保护/.test(title + desc)) return 'acceptance';
  if (/技术|管理|体系|组织|机构|制度|岗位/.test(title + desc)) return 'technical_management';
  if (/风险|危险|分析|预案/.test(title + desc)) return 'risk_management';
  if (/服务|售后|维保|培训|承诺/.test(title + desc)) return 'service_support';
  return 'other';
}

